    private void butBuscarpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butBuscarpActionPerformed
        // TODO add your handling code here:
         String sqlStmt;
            String []datos=new String[10];
              
               
        if("Buscar".equals(butBuscarp.getText()))
        {
      
             sqlStmt="Select * From  peliculas";
            //agrego la condicion del usuario
            
            sqlStmt+=" Where CodPelicula='"+txtCodPeli.getText()+"'";
           
            
            oConn.subQueryExecute(sqlStmt);
           
            try
            {
                if(oConn.setResult.next())
               {
                   
                 datos[0]=oConn.setResult.getString("CodPelicula");
                 datos[1]=oConn.setResult.getString("Titulo");
                 datos[2]=oConn.setResult.getString("Duracion");
                 datos[3]=oConn.setResult.getString("CodGenero");
                 datos[4]=oConn.setResult.getString("Precio");
                 datos[5]=oConn.setResult.getString("Director");
                 datos[6]=oConn.setResult.getString("Formato");
                 modelo.addRow(datos);
                 TbProductos.setModel(modelo);
                 
                  
                }
                else
                {
                    
                JOptionPane.showMessageDialog(null,"Pelicula no registrada");
                }
                //cierra la consulta
                oConn.setResult.close();
            
            }
            catch(SQLException e)
            {
                  JOptionPane.showMessageDialog(null,"No hay conexion");
            }
        }
        
    }//GEN-LAST:event_butBuscarpActionPerformed

