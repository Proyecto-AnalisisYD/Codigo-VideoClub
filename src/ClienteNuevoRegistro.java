          String sQuery;
        
        // Prepara el Query para Insertar
        sQuery = "Insert into clientes Values (";
        sQuery += "'"+txtID.getText().toString()+"',";
        sQuery += "'"+txtNombres.getText().toString()+"',";
        sQuery += "'"+txtApellidos.getText().toString()+"',";
