    private void butBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butBuscarActionPerformed
        // TODO add your handling code here:
           String sqlStmt;
            String []datos=new String[10];
              
               
        if("Buscar".equals(butBuscar.getText()))
        {
      
            sqlStmt="Select * From clientes";
            //agrego la condicion del usuario
            
            sqlStmt+=" Where CodCli='"+txtCodCli.getText()+"'";
           
            
            oConn.subQueryExecute(sqlStmt);
           
            try
            {
                if(oConn.setResult.next())
               {
                   
                 datos[0]=oConn.setResult.getString("CodCli");
                 datos[1]=oConn.setResult.getString("NombCli");
                 datos[2]=oConn.setResult.getString("Apellido");
                 datos[3]=oConn.setResult.getString("Direccion");
                 datos[4]=oConn.setResult.getString("correo");
                 datos[5]=oConn.setResult.getString("tel");
                 modelo.addRow(datos);
                 TbConsultas.setModel(modelo);
                 
                  
                }
                else
                {
                    
                JOptionPane.showMessageDialog(null,"Cliente no resgistrado");
                }
                //cierra la consulta
                oConn.setResult.close();
            
            }
            catch(SQLException e)
            {
                  JOptionPane.showMessageDialog(null,"No hay conexion");
            }
        }
        
        
    }//GEN-LAST:event_butBuscarActionPerformed

