    private void butExplorarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butExplorarActionPerformed
       
          String sqlStmt;
            String []datos=new String[6];
              
               
        if("Explorar".equals(butExplorar.getText()))
        {
      
            sqlStmt="Select * From peliculas";
            //agrego la condicion del usuario
            
            
            sqlStmt+=" Where CodPelicula='"+txtCodPelicula.getText()+"'";
           
            
            oConn.subQueryExecute(sqlStmt);
           
            try
            {
                if(oConn.setResult.next())
               {
                   
                 datos[0]=oConn.setResult.getString("CodPelicula");
                 datos[1]=oConn.setResult.getString("Titulo");
                 datos[2]=oConn.setResult.getString("Precio");
                 modelo.addRow(datos);
                 TbProductos.setModel(modelo);
                 
                  
                }
                else
                {
                    
                JOptionPane.showMessageDialog(null,"Pelicula no registrada");
                }
                //cierra la consulta
                oConn.setResult.close();
            
            }
            catch(SQLException e)
            {
                  JOptionPane.showMessageDialog(null,"No hay conexion");
            }
        }
        
        
        
    }//GEN-LAST:event_butExplorarActionPerformed

