    private void butBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butBuscarActionPerformed
        // TODO add your handling code here:
        String sqlStmt;
        
        if("Buscar".equals(butBuscar.getText()))
        {
            sqlStmt="Select * From clientes";
            //agrego la condicion del usuario
            
            sqlStmt+=" Where CodCli='"+txtID.getText()+"'";
            //sqlStmt+=" And strUserPass='"+String.valueOf(txtPassword.getPassword())+"'";
            
            oConn.subQueryExecute(sqlStmt);
            
            try
            {
                if(oConn.setResult.next())
                {
                   txtNombres.setText(oConn.setResult.getString("NombCli"));
                   txtApellidos.setText(oConn.setResult.getString("Apellido"));
                   //cambio el texto del boton
                
                }
                else
                {
                    
                JOptionPane.showMessageDialog(null,"Cliente no registrado");
                }
                //cierra la consulta
                oConn.setResult.close();
            
            }
            catch(SQLException e)
            {
                  JOptionPane.showMessageDialog(null, e.toString());
            }
        }
            
        
    }//GEN-LAST:event_butBuscarActionPerformed

